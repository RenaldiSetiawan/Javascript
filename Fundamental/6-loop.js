// For loop
for(let i = 0; i < 5; i++) {
    console.log(i);
}

// For of loop
let myArray = ["Luke", "Han", "Chewbacca", "Leia"];

for(const arrayItem of myArray) {
    console.log(arrayItem)
}

// While and do-while
let i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}

// Infinite loop
let a = 1;
 
while (a <= 5) {
    console.log(a);
}