// Set sederhananya merupakan kumpulan nilai (set of values).
// Set juga bersifat unik dan tidak ada duplikasi
// Set tidak memiliki urutan atau index.
const numberSet = new Set([1, 4, 6, 4, 1]);

console.log(numberSet);

// add() -> menambahkan data ke dalam Set.
numberSet.add(5);
numberSet.add(10);

console.log(numberSet);